CREATE PROCEDURE dbo.sp_ConsumePartsFromBins
    @StationID INT
AS
BEGIN
    SET NOCOUNT ON;

    IF EXISTS
    (
        SELECT 1
        FROM Bin
        WHERE StationID = @StationID
          AND CurrentCount < 1
    )
    BEGIN
        RAISERROR('Not enough parts in one or more bins.', 16, 1);
        RETURN;
    END

    UPDATE Bin
    SET CurrentCount = CurrentCount - 1
    WHERE StationID = @StationID;
END;
GO


