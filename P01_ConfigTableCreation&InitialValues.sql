        FOREIGN KEY (StationID) REFERENCES WorkStation(StationID),

    CONSTRAINT FK_Lamps_Worker
        FOREIGN KEY (WorkerID) REFERENCES Worker(WorkerID),

    CONSTRAINT FK_Lamps_Tray
        FOREIGN KEY (TrayID) REFERENCES Tray(TrayID)
