-- =========================================================
-- ASSEMBLY AREA
-- =========================================================
CREATE TABLE AssemblyArea
(
    AssemblyAreaID INT IDENTITY(1,1) PRIMARY KEY,
    TotalOrderQty INT NOT NULL DEFAULT 0,
    InProcessCount INT NOT NULL DEFAULT 0,
    ProducedCount INT NOT NULL DEFAULT 0,
    GoodLamps INT NOT NULL DEFAULT 0
