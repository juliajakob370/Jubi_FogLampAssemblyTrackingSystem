CREATE PROCEDURE dbo.sp_GetLowStockBins
AS
BEGIN
    SET NOCOUNT ON;

    SELECT 
        b.BinID,
        b.StationID,
        ws.StationName,
        b.PartID,
        p.PartName,
        b.CurrentCount,
        b.NeedsRefill,
        b.LastReplenishment
    FROM Bin b
    INNER JOIN WorkStation ws ON b.StationID = ws.StationID
    INNER JOIN Parts p ON b.PartID = p.PartID
    WHERE b.NeedsRefill = 1
    ORDER BY b.StationID, b.PartID;
END;
GO


