-- =========================================================
-- WORK STATION
-- =========================================================
CREATE TABLE WorkStation
(
    StationID INT IDENTITY(1,1) PRIMARY KEY,
    StationName VARCHAR(100) NOT NULL UNIQUE,
    Status VARCHAR(30) NOT NULL,
    AssemblyAreaID INT NULL,
    Location VARCHAR(100) NULL,

    CONSTRAINT FK_WorkStation_AssemblyArea
        FOREIGN KEY (AssemblyAreaID) REFERENCES AssemblyArea(AssemblyAreaID)
);

-- =========================================================
-- SKILLS
-- =========================================================
CREATE TABLE Skills
(
    SkillLevelID INT IDENTITY(1,1) PRIMARY KEY,
    SkillLevelName VARCHAR(50) NOT NULL UNIQUE,
    Speed DECIMAL(6,2) NOT NULL,
    DefectRate DECIMAL(6,3) NOT NULL
);

-- =========================================================
-- WORKER
-- =========================================================
CREATE TABLE Worker
(
    WorkerID INT IDENTITY(1,1) PRIMARY KEY,
    WorkerName VARCHAR(100) NOT NULL,
    SkillLevelID INT NOT NULL,
    StationID INT NOT NULL,

    CONSTRAINT FK_Worker_Skills
        FOREIGN KEY (SkillLevelID) REFERENCES Skills(SkillLevelID),

    CONSTRAINT FK_Worker_WorkStation
        FOREIGN KEY (StationID) REFERENCES WorkStation(StationID)
);

-- =========================================================
-- PARTS
-- =========================================================
CREATE TABLE Parts
(
    PartID INT IDENTITY(1,1) PRIMARY KEY,
    PartName VARCHAR(50) NOT NULL UNIQUE,
    DefaultCapacity INT NOT NULL
);


-- =========================================================
-- BIN
-- =========================================================
CREATE TABLE Bin
(
    BinID INT IDENTITY(1,1) PRIMARY KEY,
    PartID INT NOT NULL,
    StationID INT NOT NULL,
    CurrentCount INT NOT NULL,
    NeedsRefill BIT NOT NULL DEFAULT 0,
    LastReplenishment DATETIME NULL,

    CONSTRAINT FK_Bin_Parts
        FOREIGN KEY (PartID) REFERENCES Parts(PartID),

    CONSTRAINT FK_Bin_WorkStation
        FOREIGN KEY (StationID) REFERENCES WorkStation(StationID),

    CONSTRAINT UQ_Bin_Station_Part
        UNIQUE (StationID, PartID)
);

-- =========================================================
-- TRAY
-- =========================================================
CREATE TABLE Tray
(
    TrayID INT IDENTITY(1,1) PRIMARY KEY,
    CreatedTime DATETIME NOT NULL DEFAULT GETDATE(),
    Capacity INT NOT NULL,
    TrayStatus VARCHAR(30) NOT NULL,
    SentToTestingTime DATETIME NULL
);

-- =========================================================
-- LAMPS
-- =========================================================
CREATE TABLE Lamps
(
    LampID INT IDENTITY(1,1) PRIMARY KEY,
    StationID INT NOT NULL,
    WorkerID INT NOT NULL,
    TrayID INT NULL,
    IsDefective BIT NOT NULL DEFAULT 0,
