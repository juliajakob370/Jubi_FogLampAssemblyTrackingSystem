-- =========================================================
-- PROCEDURE: Refill Bin
-- =========================================================
IF OBJECT_ID('dbo.sp_RefillBin', 'P') IS NOT NULL
    DROP PROCEDURE dbo.sp_RefillBin;
GO

CREATE PROCEDURE dbo.sp_RefillBin
    @BinID INT
AS
BEGIN
    SET NOCOUNT ON;

    DECLARE @OldCount INT;
    DECLARE @PartID INT;
    DECLARE @DefaultCapacity INT;
    DECLARE @NewCount INT;

    SELECT 
        @OldCount = CurrentCount,
        @PartID = PartID
    FROM Bin
    WHERE BinID = @BinID;

    SELECT @DefaultCapacity = DefaultCapacity
    FROM Parts
    WHERE PartID = @PartID;

    SET @NewCount = @OldCount + @DefaultCapacity;

    UPDATE Bin
    SET CurrentCount = @NewCount,
        NeedsRefill = 0,
        LastReplenishment = GETDATE()
    WHERE BinID = @BinID;

    INSERT INTO RunnerActivity (BinID, ActionType, ActivityTime, OldCount, NewCount)
    VALUES (@BinID, 'Refill', GETDATE(), @OldCount, @NewCount);
END;
GO


